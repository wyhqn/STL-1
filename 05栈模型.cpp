#include<iostream>
using namespace std;
#include"stack"


//栈模型
//站的算法 和 栈容器数据类型的分离的
void main51()
{
	stack<int> s;
	for(int i=0;i<10;i++)
	{
		s.push(i+1);
	}
	cout<<"栈的大小"<<s.size()<<endl;
	//出栈
	while(!s.empty())
	{
		int temp=s.top();//获取栈顶元素
		cout<<temp<<"  ";
		s.pop(); //弹出栈顶元素

	}
}

//Teacher 结点
class Teacher
{
public:
	int age;
	char name[32];
public:
	void printT()
	{
		cout<<"age:"<<age<<endl;
	}
};
void main52()
{
	Teacher t1,t2,t3;
	t1.age=11;
	t2.age=22;
	t3.age=33;

	stack<Teacher> s;
		s.push(t1);
		s.push(t2);
		s.push(t3);

	while(!s.empty())
	{
		Teacher tmp=s.top();
			tmp.printT();
			s.pop();
	}
}

